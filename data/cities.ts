// Helper functions
export function getCityBySlug(slug: string): CityData | undefined {
  return cities.find(city => city.slug === slug);
}

export function getCityNames(): string[] {
  return cities.map(city => city.name);
}

export function getAffordableCities(): CityData[] {
  return [...cities].sort((a, b) => a.rentToIncomeRatio - b.rentToIncomeRatio).slice(0, 5);
}

export function getExpensiveCities(): CityData[] {
  return [...cities].sort((a, b) => b.avgRent1Bed - a.avgRent1Bed).slice(0, 5);
}

export function getCitiesWithDecliningRent(): CityData[] {
  return cities.filter(city => city.yoyChange < 0);
}

export function getRentFairnessScore(actualRent: number, avgRent: number): { score: number; label: string; color: string } {
  const ratio = actualRent / avgRent;
  if (ratio <= 0.85) return { score: 95, label: "Great Deal", color: "text-green-600" };
  if (ratio <= 0.95) return { score: 80, label: "Below Average", color: "text-green-500" };
  if (ratio <= 1.05) return { score: 65, label: "Fair Price", color: "text-yellow-500" };
  if (ratio <= 1.15) return { score: 40, label: "Above Average", color: "text-orange-500" };
  return { score: 20, label: "Overpriced", color: "text-red-500" };
}
